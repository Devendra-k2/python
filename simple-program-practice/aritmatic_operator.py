num1=int(input("Enter first number"))
num2=int(input("Enter second number"))

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2)  #"//" is used to remove the decimal points
print(num1 % num2)
print(num1 ** num2)  #"**" is used to assign power(num1 to the power num2)


